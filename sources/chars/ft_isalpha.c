#include "../../includes/libft.h"

int	ft_isalpha(int c)
{
	if (c == EOF || (c >= 0 && c <= 255))
	{
		if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
			return (1);
	}
	return (0);
}
